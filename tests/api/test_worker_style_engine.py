def _style_engine_cfg(rules_file, phases=None, enabled=True, keep_raw_on_fix=True, qa_gate=None):
    cfg = {
        "enabled": enabled,
        "rules_file": str(rules_file),
        "keep_raw_on_fix": keep_raw_on_fix,
        "phases": phases or {},
    }
    if qa_gate is not None:
        cfg["qa_gate"] = qa_gate
    return cfg


def _write_rules(tmp_path, title_max=80, short_max=90, long_max=350):
    """Minimal house-style YAML covering both pre-stage and post-stage needs.
    Mirrors the synthetic StyleRules built inline in tests/test_style_stages.py."""
    raw = {
        "meta": {"version": 1},
        "voice": {
            "forbidden_phrases": [
                {"match": "discover", "category": "viewer_directive", "severity": "error"},
            ],
            "first_person_markers": [r"\bwe\b", r"\bour\b"],
            "second_person_markers": [r"\byou\b"],
        },
        "casing": {
            "style": "down",
            "proper_nouns": ["Wisconsin", "Governor", "Madison"],
            "acronyms": ["PBS"],
            "casing_variants": {},
            "surname_stoplist": [],
        },
        "limits": {
            "fields": {
                "title": {"max": title_max},
                "short_description": {"max": short_max},
                "long_description": {"max": long_max},
            },
            "content_type_overrides": {},
        },
        "programs": {},
    }
    path = tmp_path / "house_style.yaml"
    path.write_text(yaml.safe_dump(raw))
    return path


