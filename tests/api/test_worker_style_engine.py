"""Tests for the worker's style-engine pre/post hooks (routing.style_engine).

Wires the pure `run_pre_stage`/`run_post_stage` pipeline stages
(api/services/style_engine/{pre_stage,post_stage}.py) into `JobWorker._run_phase`
behind default-off `routing.style_engine` config flags. Covers:

1. Kill switch -- missing config block, `enabled: false`, or phase mode "off"
   must produce byte-for-byte today's behavior (proven separately by the
   unmodified tests/api/test_worker.py suite staying green; also asserted
   directly here for the persisted-file format).
2. Pre-generation hook -- prompt-section injection into the built user
   prompt, and the stale-state guard that pops any leftover `style_pre` from
   a previous phase.
3. Post-generation hook -- shadow mode (record-only, raw content flows) vs.
   enforce mode (normalized content persists, `.raw.md` archive, provenance
   line) vs. fail-open on any exception (missing rules file, event-logging
   failure).

Follows tests/api/test_worker.py's fixture/mocking conventions (mock_llm_client,
mock_llm_response, AGENTS_DIR patched to redirect into tmp_path).
"""

