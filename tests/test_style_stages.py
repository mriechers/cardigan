class TestPreStageSeoData:
    def test_char_budgets_not_hardcoded(self):
        rules = _pre_rules(title_max=42)
        result = run_pre_stage("seo", {}, rules)
        assert result.data["char_budgets"]["title"] == 42
        assert result.data["char_budgets"]["short_description"] == 90
        assert result.data["char_budgets"]["long_description"] == 350
        # keywords is a count-limit (list field), not a char budget.
        assert "keywords" not in result.data["char_budgets"]

    def test_proper_nouns_from_analyst_table(self):
        rules = _pre_rules()
        result = run_pre_stage("seo", {"analyst_output": ANALYST_TABLE}, rules)
        assert "Nick Hoffman" in result.data["proper_nouns"]
        assert "Angela Fitzgerald" in result.data["proper_nouns"]

    def test_keyword_candidates_only_transcript_verified(self):
        rules = _pre_rules()
        result = run_pre_stage(
            "seo",
            {"analyst_output": ANALYST_TABLE, "transcript": TRANSCRIPT_TEXT},
            rules,
        )
        assert "Nick Hoffman" in result.data["keyword_candidates"]
        assert "Angela Fitzgerald" not in result.data["keyword_candidates"]

    def test_program_rules_here_and_now(self):
        rules = _pre_rules()
        result = run_pre_stage("seo", {"program": "Here & Now"}, rules)
        assert result.data["program_rules"]["short_description_formula"] == "{role} {verb} {subject}."

    def test_program_rules_empty_for_unknown_program(self):
        rules = _pre_rules()
        result = run_pre_stage("seo", {"program": "Some Other Show"}, rules)
        assert result.data["program_rules"] == {}

    def test_program_rules_empty_when_no_program(self):
        rules = _pre_rules()
        result = run_pre_stage("seo", {}, rules)
        assert result.data["program_rules"] == {}


