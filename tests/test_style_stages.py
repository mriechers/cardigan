class TestPostStageUnparseable:
    def test_unparseable_report_passthrough_no_raise(self):
        rules = _post_rules()
        raw_output = "# SEO Report\n\nNo recommendations generated.\n"

        result = run_post_stage("seo", raw_output, {}, rules)

        assert result.check.parse_ok is False
        assert result.normalized_output == raw_output
        assert result.changed is False
        assert len(result.check.violations) == 1
        violation = result.check.violations[0]
        assert violation.rule_id == "phase_io.seo.unparseable"
        assert violation.severity == "warning"
        assert violation.model_fixable is True

    def test_empty_report_does_not_raise(self):
        rules = _post_rules()
        result = run_post_stage("seo", "", {}, rules)
        assert result.check.parse_ok is False
        assert result.normalized_output == ""


