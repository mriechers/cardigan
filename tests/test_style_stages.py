class TestPostStageEndToEndNormalize:
    def test_over_capitalized_title_normalized_and_spliced(self):
        rules = _post_rules()
        raw_title = "Wisconsin Governor Signs New Budget Bill In Madison"
        raw_output = _seo_report(raw_title)

        result = run_post_stage("seo", raw_output, {}, rules)

        assert result.changed is True
        assert len(result.check.fixes) == 1
        fix = result.check.fixes[0]
        assert fix.rule_id == "casing.down_style.title"
        assert fix.before == raw_title

        expected_title = "Wisconsin Governor signs new budget bill in Madison"
        assert fix.after == expected_title

        reparsed = extract_seo_fields(result.normalized_output)
        assert reparsed.title.value == expected_title

        # Rest of document is byte-identical outside the title's span.
        raw_fields = extract_seo_fields(raw_output)
        assert result.normalized_output[: raw_fields.title.start] == raw_output[: raw_fields.title.start]
        tail_start_new = raw_fields.title.start + len(expected_title)
        assert result.normalized_output[tail_start_new:] == raw_output[raw_fields.title.end :]

    def test_convergence_over_cap_and_over_lower_same_result(self):
        rules = _post_rules()
        over_cap = _seo_report("Wisconsin Governor Signs New Budget Bill In Madison")
        over_lower = _seo_report("wisconsin governor signs new budget bill in madison")

        result_cap = run_post_stage("seo", over_cap, {}, rules)
        result_lower = run_post_stage("seo", over_lower, {}, rules)

        title_cap = extract_seo_fields(result_cap.normalized_output).title.value
        title_lower = extract_seo_fields(result_lower.normalized_output).title.value
        assert title_cap == title_lower

    def test_already_clean_title_unchanged(self):
        rules = _post_rules()
        title = "Wisconsin Governor signs new budget bill in Madison"
        raw_output = _seo_report(title)

        result = run_post_stage("seo", raw_output, {}, rules)

        assert result.changed is False
        assert result.normalized_output == raw_output
        assert result.check.fixes == []


